	protected function render() {
		$settings = $this->get_settings_for_display();
		$target = $settings['btn_link']['is_external'] ? ' target="_blank"' : '';
		$nofollow = $settings['btn_link']['nofollow'] ? ' rel="nofollow"' : '';
		
		$output = '';
		
		if($settings['box_type'] == 'type2'){
			$boxtype = 'medium';
		} else {
			$boxtype = 'large';
		}
		
		echo '<div class="klb-img-box banner-item '.esc_attr($boxtype).'">';
		echo '<div class="banner-text">';
		echo '<h3 class="entry-title">'.esc_html($settings['title']).'</h3>';
		echo '<div class="entry-description">';
		echo '<p>'.esc_html($settings['desc']).'</p>';
		echo '</div>';
		echo '<a href="'.esc_url($settings['btn_link']['url']).'" '.esc_attr($target.$nofollow).' class="button-text">';
		echo '<span>'.esc_html($settings['btn_title']).'</span>';
		echo '<i class="klb-right"></i>';
		echo '</a>';
		echo '</div>';
		echo '<div class="banner-image"><img src="'.esc_url($settings['image']['url']).'" alt="img"></div>';
		echo '<a href="'.esc_url($settings['btn_link']['url']).'" '.esc_attr($target.$nofollow).' class="overlay-link"></a>';
		echo '</div>';

	}

